package BitManipulation;

public class Basics {
    public static void main(String[] args) {
        int num1 = 5;
        int num2 = 6;
        System.out.println(num1 & num2);
        System.out.println(num1 | num2);
        System.out.println(num1 ^ num2);
        System.out.println(~num1);
        System.out.println(16 << 3);
        System.out.println(25 >> 4);
    }
}
